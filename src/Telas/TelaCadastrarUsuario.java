public void readjTable() {
        DefaultTableModel modelo = (DefaultTableModel) jUsuario.getModel();
        modelo.setNumRows(0);
        UsuarioDao udao = new UsuarioDao();

        for (Usuario u : udao.read()) {

            modelo.addRow(new Object[]{
                u.getNome_usuario(),
                u.getLogin(),
                u.getSenha(),
                u.getPerfil(),
            });

        }
    }
