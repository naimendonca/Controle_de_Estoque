    private void jButton_usuario__alterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_usuario__alterarActionPerformed
        if (jUsuario.getSelectedRow() != -1) {
        
        u.setLogin(txt_usuario_login.getText());
        u.setNome_usuario(txt_usuario_nome.getText());
        u.setSenha(txt_usuario_senha.getText());
        if (jRadioButtonAdm.isSelected()) {
            u.setPerfil("Administrador");

        }
        else if (jRadioButtonPad.isSelected()) {

            u.setPerfil("Usuario Padrao");

        }
        else {

           u.setPerfil("Usuário Restrito");

        }
        
        dao.update(u);
        
        txt_usuario_login.setText("");
        txt_usuario_nome.setText("");
        txt_usuario_senha.setText("");
        
        readjTable();
        }
    }//GEN-LAST:event_jButton_usuario__alterarActionPerformed

    private void jButton_usuario__excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_usuario__excluirActionPerformed
        if (jUsuario.getSelectedRow() != -1) {
        
        u.setLogin(txt_usuario_login.getText());
        dao.delete(u);
        
        txt_usuario_login.setText("");
        txt_usuario_nome.setText("");
        txt_usuario_senha.setText("");
        
        readjTable();
        }
    }//GEN-LAST:event_jButton_usuario__excluirActionPerformed

