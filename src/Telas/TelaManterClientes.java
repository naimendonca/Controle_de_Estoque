    private void jClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jClienteMouseClicked
        if (jCliente.getSelectedRow() != -1) {

            txt_cliente_cod.setText(jCliente.getValueAt(jCliente.getSelectedRow(), 0).toString());
            txt_cliente_nome.setText(jCliente.getValueAt(jCliente.getSelectedRow(), 1).toString());
            txt_cliente_cnpj.setText(jCliente.getValueAt(jCliente.getSelectedRow(), 2).toString());
           

        }
    }//GEN-LAST:event_jClienteMouseClicked

