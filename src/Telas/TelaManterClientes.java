    public void readjTable() {
        DefaultTableModel modelo = (DefaultTableModel) jCliente.getModel();
        modelo.setNumRows(0);
        ClienteDao cdao = new ClienteDao();

        for (Clientes c : cdao.read()) {

            modelo.addRow(new Object[]{
                c.getCod_cliente(),
                c.getNome_cliente(),
                c.getCnpj(),
            
            });

        }
    }

