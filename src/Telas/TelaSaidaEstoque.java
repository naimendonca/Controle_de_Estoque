    private void jButton_saida1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_saida1ActionPerformed
       saida.setCod_saida(txt_cod_saida.getText());
       saida.setArmazenamento((Armazenamento) jComboBox_armazenamento.getSelectedItem());
       saida.setCliente((Clientes) jComboBox_cliente.getSelectedItem());
       saida.setData(txt_data.getText());
       saida.setQtd(Integer.parseInt(txt_qtd.getText()));
        try {
            saida.ConferirEstoque();
            dao.create(saida);
            JOptionPane.showMessageDialog(null, "Favor, atualizar o valor do armazenamento");
            new TelaEntradaEstoque().setVisible(true);
            
       
            //AtualizaArmazenamento();
        } catch (Exception ex) {
            Logger.getLogger(TelaSaidaEstoque.class.getName()).log(Level.SEVERE, null, ex);
        }
       

       txt_cod_saida.setText("");
       txt_data.setText("");
       txt_qtd.setText("");
        
       readjTable();
    }//GEN-LAST:event_jButton_saida1ActionPerformed

