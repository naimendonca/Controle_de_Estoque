     public void readjTable() {
        DefaultTableModel modelo = (DefaultTableModel) jSaida.getModel();
        modelo.setNumRows(0);
       
        SaidaDao sdao = new SaidaDao();

        for (Saida s : sdao.read()) {

            modelo.addRow(new Object[]{
                s.getCod_saida(),
                s.getArmazenamento(),
                s.getCliente(),
                s.getQtd(),
                s.getData(),
            });

        }
    }

