    private void jSaidaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jSaidaMouseClicked
       if (jSaida.getSelectedRow() != -1) {

            txt_cod_saida.setText(jSaida.getValueAt(jSaida.getSelectedRow(), 0).toString());
            jComboBox_armazenamento.setSelectedItem(jSaida.getValueAt(jSaida.getSelectedRow(), 1).toString());
            jComboBox_cliente.setSelectedItem(jSaida.getValueAt(jSaida.getSelectedRow(), 2).toString());
            txt_qtd.setText(jSaida.getValueAt(jSaida.getSelectedRow(), 3).toString());
            txt_data.setText(jSaida.getValueAt(jSaida.getSelectedRow(), 4).toString());
        }
    }//GEN-LAST:event_jSaidaMouseClicked

