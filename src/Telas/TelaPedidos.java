        public void readjTable() {
        DefaultTableModel modelo = (DefaultTableModel) jPedidos.getModel();
        modelo.setNumRows(0);
       
        PedidoDao pedao = new PedidoDao();

        for (Pedidos pe : pedao.read()) {

            modelo.addRow(new Object[]{
                pe.getCod_pedido(),
                pe.getProduto(),
                pe.getCliente(),
                pe.getQtd(),
                pe.getData(),
            });

        }
    }

