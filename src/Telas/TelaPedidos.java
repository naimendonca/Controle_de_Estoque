    private void jPedidosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPedidosMouseClicked
        if (jPedidos.getSelectedRow() != -1) {

            txt_pedido.setText(jPedidos.getValueAt(jPedidos.getSelectedRow(), 0).toString());
            jComboBox_produto.setSelectedItem(jPedidos.getValueAt(jPedidos.getSelectedRow(), 1).toString());
            jComboBox_cliente.setSelectedItem(jPedidos.getValueAt(jPedidos.getSelectedRow(), 2).toString());
            txt_qtd.setText(jPedidos.getValueAt(jPedidos.getSelectedRow(), 3).toString());
            txt_data.setText(jPedidos.getValueAt(jPedidos.getSelectedRow(), 4).toString());

        }
    }//GEN-LAST:event_jPedidosMouseClicked

