    private void jButton_produtos_cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_produtos_cadastrarActionPerformed
        pedido.setCod_pedido(txt_pedido.getText());
        pedido.setProduto((Produtos) jComboBox_produto.getSelectedItem());
        pedido.setCliente((Clientes) jComboBox_cliente.getSelectedItem());
        pedido.setQtd(Integer.parseInt(txt_qtd.getText()));
        pedido.setData(txt_data.getText());
        
        dao.create(pedido);

        txt_pedido.setText("");
        txt_qtd.setText("");
        txt_data.setText("");

        readjTable();

    }//GEN-LAST:event_jButton_produtos_cadastrarActionPerformed

