    public void readjTable() {
        DefaultTableModel modelo = (DefaultTableModel) jProdutos.getModel();
        modelo.setNumRows(0);
        ProdutoDao pdao = new ProdutoDao();

        for (Produtos p : pdao.read()) {

            modelo.addRow(new Object[]{
                p.getCodigo_produto(),
                p.getDescricao_produto(),
                p.getCategoria(),
            });

        }
    }

