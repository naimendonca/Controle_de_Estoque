            txt_produtos_codigo.setText(jProdutos.getValueAt(jProdutos.getSelectedRow(), 0).toString());
            txt_produtos_descricao.setText(jProdutos.getValueAt(jProdutos.getSelectedRow(), 1).toString());
            jComboBox_categoria.setSelectedItem(jProdutos.getValueAt(jProdutos.getSelectedRow(), 2).toString());

