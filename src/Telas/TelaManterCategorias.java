    private void jButton_Alterar_categoriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_Alterar_categoriaActionPerformed
        
        if (jCategorias.getSelectedRow() != -1) {
        c.setCodigo_categoria(txt_categoria_codigo.getText());
        c.setNome_categoria(txt_categoria_nome.getText());
        
        dao.update(c);
        
        txt_categoria_codigo.setText("");
        txt_categoria_nome.setText("");
        
        readjTable();
         }
    }//GEN-LAST:event_jButton_Alterar_categoriaActionPerformed

    private void jButton_excluir_categoriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_excluir_categoriaActionPerformed
        if (jCategorias.getSelectedRow() != -1) {
        c.setCodigo_categoria(txt_categoria_codigo.getText());
        
        dao.delete(c);
        
        txt_categoria_codigo.setText("");
        txt_categoria_nome.setText("");
        
        readjTable();
        }
    }//GEN-LAST:event_jButton_excluir_categoriaActionPerformed

