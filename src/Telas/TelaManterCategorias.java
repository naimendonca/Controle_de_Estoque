    public void readjTable() {
        DefaultTableModel modelo = (DefaultTableModel) jCategorias.getModel();
        modelo.setNumRows(0);
        CategoriaDao cdao = new CategoriaDao();

        for (Categorias c : cdao.read()) {

            modelo.addRow(new Object[]{
                c.getCodigo_categoria(),
                c.getNome_categoria(),});

        }
    }
    
  

